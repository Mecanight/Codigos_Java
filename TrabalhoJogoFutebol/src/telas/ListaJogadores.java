    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // TODO add your handling code here:
        if (jtLista.getSelectedRowCount() > 0) {
            ArrayList<Jogador> paraExcluir = new ArrayList<Jogador>();
            for (int index : jtLista.getSelectedRows()) {
                paraExcluir.add(lista.get(index));
            }
            int opc = 0;
            opc = JOptionPane.showConfirmDialog(this, "Confirma exclusão de registros?");
            if (opc == 0) {
                for (Jogador jog : paraExcluir) {
                    lista.remove(jog);
                }
                lista();
            }

        } else {
            JOptionPane.showMessageDialog(this,
                    "Selecione pelo menos um jogador para excluir.");
        }
    }//GEN-LAST:event_btExcluirActionPerformed

