    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        if (jtLista.getSelectedRowCount() == 1) {
            CadastroJogador cadastro = new CadastroJogador(
                    null, true, lista,
                    jtLista.getSelectedRow());
            cadastro.setVisible(true);
            lista();
        } else {
            JOptionPane.showMessageDialog(this, "Selecione o jogador para alterar.");
        }
    }//GEN-LAST:event_btAlterarActionPerformed

    private void btInserirJogTimeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInserirJogTimeActionPerformed
// TODO add your handling code here:
        if (jtLista.getSelectedRowCount() >= 1) {
            ArrayList<Jogador> paraIncluir = new ArrayList();
            for (int index : jtLista.getSelectedRows()) {
                paraIncluir.add(lista.get(index));
            }
            int opc = 0;
            opc = JOptionPane.showConfirmDialog(this, "Confirma inclusão de registros?");
            if (opc == 0) {
                for (Jogador jog : paraIncluir) {
                    timeaux.getPlantel().add(jog);
                }
                lista();
            }
            lista();
        } else {
            JOptionPane.showMessageDialog(this, "Selecione pelo menos um jogador da lista.");
        }

    }//GEN-LAST:event_btInserirJogTimeActionPerformed

    public void lista() {

        DefaultTableModel model = (DefaultTableModel) jtLista.getModel();
        model.getDataVector().clear();
        for (Jogador obj : lista) {
            model.addRow(
                    new Object[]{
                        obj.getNome(),
                        obj.getApelido(),
                        obj.getPosicao(),
                        obj.getQualidade()
                    });
        }
    }

