    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // TODO add your handling code here:
        String nome = tfNome.getText();        
        String apelido = tfApelido.getText();        
        String escudo = tfEscudo.getText();        
        timeaux = new Time();
        timeaux.setApelido(apelido);
        timeaux.setEscudo(escudo);
        timeaux.setNome(nome);
        timeaux.setFundacao(new Date());
        timeaux.setPlantel(new ArrayList());
        timeaux.setRelacionados(new ArrayList());
        if (nome.equals("")) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "o nome não pode ser vazio, redigite...!");
        } else {
            if (novo == true) {
                listata.add(timeaux);
                i = listata.size() - 1;
            } else {
                listata.set(i, timeaux);
            }
            novo = false;
        }
        ativaNavegacao();
    }//GEN-LAST:event_btSalvarActionPerformed

