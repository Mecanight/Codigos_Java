    private void carregaDados() {
        // recupera os dados do vetor e atribui para um 
        // objeto local        
        timeaux = listata.get(i);
        tfNome.setText(timeaux.getNome());
        tfApelido.setText(timeaux.getApelido());
        tfEscudo.setText(timeaux.getEscudo());        
    }

    private void limpaTela() {
        tfNome.setText("");
        tfApelido.setText("");
        tfEscudo.setText("");                
        tfNome.requestFocus();
    }

