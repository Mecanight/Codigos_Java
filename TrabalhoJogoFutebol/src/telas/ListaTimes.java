    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // TODO add your handling code here:
        if (jtLista.getSelectedRowCount() > 0) {
            ArrayList<Time> paraExcluir = new ArrayList<Time>();
            for (int index : jtLista.getSelectedRows()) {
                paraExcluir.add(lista.get(index));
            }
            int opc = 0;
            opc = JOptionPane.showConfirmDialog(this, "Confirma exclus√£o de registros?");
            if (opc == 0) {
                for (Time time : paraExcluir) {
                    lista.remove(time);
                }
                lista();
            }

        } else {
            JOptionPane.showMessageDialog(this,
                    "Selecione pelo menos um time para excluir.");
        }
    }//GEN-LAST:event_btExcluirActionPerformed

